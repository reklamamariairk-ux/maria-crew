-- ── Таблица выполнения сезонных челленджей ────────────────────────────────────
CREATE TABLE IF NOT EXISTS seasonal_challenge_entries (
  id           SERIAL PRIMARY KEY,
  challenge_id INTEGER NOT NULL REFERENCES seasonal_challenges(id) ON DELETE CASCADE,
  employee_id  INTEGER NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  completed_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  card_awarded BOOLEAN NOT NULL DEFAULT false,
  UNIQUE (challenge_id, employee_id)
);

CREATE INDEX IF NOT EXISTS idx_challenge_entries_emp ON seasonal_challenge_entries(employee_id);

