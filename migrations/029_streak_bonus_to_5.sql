-- Бонус за 7 дней серии понижен с 20 до 5 монет.
-- Обновляем вопрос квиза, в котором правильный ответ зависит от размера бонуса.
-- До этой миграции в вопросе был правильный ответ «10 монет» (индекс 3),
-- что было ошибочно даже относительно прежней механики (+20).

UPDATE quiz_questions
SET options = '["1 монету","5 монет","7 монет","10 монет"]'::jsonb,
    correct_index = 1
WHERE question = 'Сколько монет даёт серия входов 7 дней подряд в Maria Crew?';
