-- Переименование «MVP» / «Топ-точка» в текстах вопросов квиза.
-- Внутренние identifier'ы (поля is_mvp, source='mvp', enum значения) не трогаем —
-- меняется только то, что видно сотруднику.

-- 1) «Что такое MVP в системе Maria Crew?» → формулировка про лучшего сотрудника
UPDATE quiz_questions
SET question = 'Кого в Maria Crew называют лучшим сотрудником месяца?',
    options  = '["Самого опытного по стажу","Дежурного на смене","Сотрудника с лучшим суммарным результатом по показателям","Самого молодого в команде"]'::jsonb,
    correct_index = 2
WHERE question = 'Что такое MVP в системе Maria Crew?';

-- 2) Вариант ответа «MVP» в вопросе про FIFO — заменяем на нейтральный, чтобы
-- варианты выглядели правдоподобно (вариант не правильный)
UPDATE quiz_questions
SET options = '["LIFO","FIFO","ABC","JIT"]'::jsonb
WHERE question = 'Как называется способ хранения «первым пришёл — первым продан»?'
  AND options::text LIKE '%MVP%';

-- 3) «Что значит «Топ-точка месяца» в Maria Crew?» → переформулировать
UPDATE quiz_questions
SET question = 'Что значит «Лучшая точка месяца» в Maria Crew?',
    options  = '["Точка с самой высокой арендой","Точка с лучшим суммарным результатом по всем показателям","Точка с наибольшей площадью","Точка с самым старым оборудованием"]'::jsonb,
    correct_index = 1
WHERE question = 'Что значит «Топ-точка месяца» в Maria Crew?';

-- 4) «Закрыть месяц: подвести итоги, начислить MVP...» — переформулировать вариант
UPDATE quiz_questions
SET options = '["Удалить старые данные","Закрыть месяц: подвести итоги, начислить награды лучшему сотруднику и команде лучшей точки","Обновить цены в магазине","Перезапустить сервер"]'::jsonb
WHERE question = 'Что такое «обработать месяц» в админке Maria Crew?';
