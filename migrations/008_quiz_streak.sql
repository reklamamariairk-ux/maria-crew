-- Добавляем типы монет (PostgreSQL 12+ supports ALTER TYPE ADD VALUE in transactions)
ALTER TYPE coin_reason ADD VALUE IF NOT EXISTS 'quiz';
ALTER TYPE coin_reason ADD VALUE IF NOT EXISTS 'checkin';

-- Вопросы для квиза
CREATE TABLE IF NOT EXISTS quiz_questions (
  id            SERIAL PRIMARY KEY,
  question      TEXT NOT NULL,
  options       JSONB NOT NULL,           -- массив из 4 строк
  correct_index INTEGER NOT NULL CHECK (correct_index BETWEEN 0 AND 3),
  category      VARCHAR(50) NOT NULL DEFAULT 'product',
  is_active     BOOLEAN NOT NULL DEFAULT true,
  created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Ответы сотрудников
CREATE TABLE IF NOT EXISTS quiz_attempts (
  id          SERIAL PRIMARY KEY,
  employee_id INTEGER NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  question_id INTEGER NOT NULL REFERENCES quiz_questions(id),
  is_correct  BOOLEAN NOT NULL,
  coins_earned INTEGER NOT NULL DEFAULT 0,
  answered_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_quiz_attempts_emp_date ON quiz_attempts(employee_id, answered_at);

-- Ежедневные входы (для серии)
CREATE TABLE IF NOT EXISTS daily_checkins (
  id           SERIAL PRIMARY KEY,
  employee_id  INTEGER NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  checkin_date DATE NOT NULL DEFAULT CURRENT_DATE,
  streak_day   INTEGER NOT NULL DEFAULT 1,
  coins_earned INTEGER NOT NULL DEFAULT 0,
  UNIQUE(employee_id, checkin_date)
);

CREATE INDEX IF NOT EXISTS idx_daily_checkins_emp ON daily_checkins(employee_id, checkin_date DESC);

